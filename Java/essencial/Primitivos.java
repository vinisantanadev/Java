package essencial;


public class Primitivos {
    public static void main(String[] args) {
        int numero = 64; // podem ser armazenado até 4 bites de numeros
        // int é um tipo primitivo
        // para usar funções em variaveis do tipo primitivo, declarar a variavel com 'Integer'
        char letra = 'W'; 
        // *bom lembrar que chars são delcarados com aspas simples ai por algum motivo*
        float decimal = 13.8f; // podem ser armazenado até 4 bites de numeros
        // *em numeros de tipo float, deve ser colocado um f no final
        double numerosDecimaisGrandes = 1346480464.89639639639396396396396393; // podem ser armazenado até 8 bites de numeros
        // para numeros decimais acima de cento e pouco
        long inteiroLongo = 546468464648L; // podem ser armazenado até 8 bites de numeros
        // tem que ter um L no final
        // * um L capitalista!!! FAÇA O L CAPITALISTA IMEDIATAMENTE

        String nome = "Whitestar";
        // BTW string não é um tipo primitivo.
            // tipos primitivos são aqueles que não podem ser quebrados em outros tipos primitivos. Ex:
                // poderiamos 'quebrar' a string "Whitestar" em 9 chars (caracteres):
                /*
                    char 1 = W
                    char 2 = H
                    char 3 = I
                    char 4 = T
                    e assim por diante
                */
        // ou seja (voce ja agradeceu a um herdeiro hoje? voce ja agradeceu a uma classe filho hoje?)
        // ou seja char é um tipo primitivo, como podemos ver
    }
}
